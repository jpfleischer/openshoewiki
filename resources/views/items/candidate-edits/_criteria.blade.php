<div class="alert alert-light border shadow-sm">
    <h2 class="h5 mb-2">How Candidate Edits Work</h2>
    <p class="text-muted mb-2">
        Phase 1 keeps proposed edits inside OpenShoeWiki. Community members can review the proposal and vote it up or down.
    </p>
    <p class="text-muted mb-2">An edit will auto-apply only if all of these are true:</p>
    <ul class="text-muted mb-0">
        <li>The 24-hour voting window must have ended.</li>
        <li>Approval votes must outnumber rejection votes.</li>
        <li>A minimum total vote count must be reached.</li>
        <li>A minimum approval margin must be reached.</li>
        <li>Only low-risk or medium-risk edits will be eligible for auto-apply.</li>
        <li>High-risk edits will require staff review.</li>
    </ul>
</div>
